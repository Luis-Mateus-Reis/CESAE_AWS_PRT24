#Ler comprimento do retângulo
comprimento = float(input("Introduza o comprimento do retângulo: "))
#Ler largura do retângulo
largura = float(input("Introduza a largura do retângulo: "))
#Cálculo e output da área do retângulo
print(f"A área do retângulo é {comprimento*largura:.1f}")
#Cálculo e output do perímetro do retângulo
print(f"O perímetro do retângulo é {2*comprimento+2*largura:.1f}")
